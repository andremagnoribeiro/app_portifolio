import React, { useState, useEffect } from 'react'

import AppBar from '@material-ui/core/AppBar';
import Toolbar from '@material-ui/core/Toolbar';
import Typography from '@material-ui/core/Typography';
import Button from '@material-ui/core/Button';
import IconButton from '@material-ui/core/IconButton';
import MenuIcon from '@material-ui/icons/Menu';


import Menu from '@material-ui/core/Menu';
import MenuItem from '@material-ui/core/MenuItem';
import { Link } from 'react-router-dom';


function HeaderMenu() {


  window.addEventListener('resize', function () {
   
    if (window.innerWidth <= 800) {
      setMenuIcon("block");
    } else {
      setMenuIcon("none");
    }
  });

  useEffect(() => {
    if (window.innerWidth <= 800) {
      setMenuIcon("block");
    } else {
      setMenuIcon("none");
    }

  }, [])

  const [anchorEl, setAnchorEl] = useState(null);

  const [menuIcon, setMenuIcon] = useState('none');




  const handleClick = (event) => {
    setAnchorEl(event.currentTarget);
  };

  const handleClose = () => {
    setAnchorEl(null);
  };

  return (


    <AppBar position="relative" style={{ width: "100%" }}>
      <Toolbar>
        <IconButton edge="start" color="inherit" aria-label="menu">
          <MenuIcon style={{ display: String(menuIcon) }} onClick={handleClick} />
          <Menu
            id="simple-menu"
            anchorEl={anchorEl}
            keepMounted
            open={Boolean(anchorEl)}
            onClose={handleClose}
          >
           
            <MenuItem onClick={handleClose}><Link to={`/`}>Trabalhos Realizados</Link></MenuItem>
            <MenuItem onClick={handleClose}><Link to={`/importxml`}>Import XML</Link></MenuItem>
          </Menu>
        </IconButton>
       
        <img  alt="logo ufjf" style={{height:40, margin:10}} src="https://doity.com.br/media/doity/eventos/evento-19789-logo_organizador.png" />
         
          
        <Typography component={'span'} style={{ display: String(menuIcon) === "block" ? "none" : "block", marginRight: "5px" }} variant="h6" >
          <Link component={Button} to={`/`}>Portfolio</Link>
          <Link component={Button} to={`/importxml`}>Import XML</Link>
        </Typography>
        <Typography component={'span'} style={{ display: String(menuIcon) === "block" ? "none" : "block", marginRight: "5px" }} variant="h6">

        </Typography>

        <Button>Login</Button>
      </Toolbar>

    </AppBar>

  );
}

export default HeaderMenu


